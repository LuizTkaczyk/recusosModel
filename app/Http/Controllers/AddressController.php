    {
        $address = Address::find($id);
        echo "<h1>Endereço de entrega</h1>";
        echo "<p>Endereço completo : {$address->address} , nº {$address->number} - cep: {$address->zipcode}</p>";
        echo "<p>Cidade: {$address->city} - UF: {$address->state}</p>";

        $addressUser = $address->user()->get()->first();
        
        if($addressUser){
            echo "<h1>Dados do Usuário</h1><br>";
            echo "<p>Nome do usuario : {$addressUser->name}</p>";
            echo "<p>Email do usuario : {$addressUser->email}</p>";
        }
    }

